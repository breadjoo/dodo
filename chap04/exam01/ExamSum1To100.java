package chap04.exam01;

public class ExamSum1To100 {

	public static void main(String[] args) {
		int x;
		int sum = 0;
		for(x=1;x<=100;x++) {
			sum += x;
		
		} System.out.println(sum);
		int a=1;
		int sum1 = 0;
		while (a<=100) {
			sum1 += a;
			a++;
		} System.out.println(sum1);
	}

}
