package S0421.exam01;

public class Multiplication1 {

	public static void main (String [] args) {
		
		int x ;
		int y ;
		for(x=1;x<9;x++) {
			System.out.println("=========="+x+"단==========");
		for(y=1;y<9;y++)
			System.out.println(x+"X"+y+"="+x*y);
		}
		
			
		 
	}
	/*for문 복습
	 * for를 열면 무조건 {} 를 열어야 한다고 생각했는데, 사용방법에 따라 안열어도 되는구나.
	 * 구구단 만들기에서 어떻게 만들어야 할 지 생각 안하고 무작정 코드를 짜다보니 막막했다.
	 */
	
}
