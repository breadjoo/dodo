package chap02.sec02;

public class IntegerLiteralExample {

	public static void main(String[] args) {

		int var1 = 0b1011; // 2진수
		int var2 = 0206; // 8진수
		int var3 = 365; // 10진수
		int var4 = 0xB3; // 16진수
		
		System.out.println("2진수(var1)0b1011 =" + var1);
		System.out.println("8진수(var2)0206 = " + var2);
		System.out.println("10진수(var3)365 = " + var3);
		System.out.println("16진수(var4)0xB3 = " + var4);
	}

}
