package chap02.sec03;

public class CharExample {

	public static void main(String[] args) {
		char c1 = 'A'; //문자 저장
		char c2 = 65; // A의 유니코드 직접 저장
		char c3 = '가';
		char c4 = 44032;
		char c5 = 604;
		
		System.out.println("char 'A' = " + c1);
		System.out.println("char 65 = " + c2);
		System.out.println("char '가' = " + c3);
		System.out.println("char 44032 = " + c4);
		System.out.println(c5);

	}

}
